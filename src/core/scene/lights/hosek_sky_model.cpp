void bake_hosek_sky_configuration(HosekSkyModel& model) {
	const int intTurbidity = static_cast<int>(model.turbidity);
	const float turbidityRem = model.turbidity - static_cast<float>(intTurbidity);
	const float solarElevation = std::pow(2.f * model.elevation / ei::PI, 1.f / 3.f);

	const float* rgbs[] = { datasetRGB1, datasetRGB2, datasetRGB3 };
	const float* rads[] = { datasetRGBRad1, datasetRGBRad2, datasetRGBRad3 };

	// Bake for low/high turb at albedo 0/1 and for each RGB channel
	for(unsigned i = 0u; i < 3u; ++i) {
		// Config
		for(unsigned int j = 0u; j < sizeof(model.configs) / sizeof(*model.configs); ++j) {
			model.configs[j][i] = 0.f;
			bake_hosek_sky_configuration(model.configs[j][i], j, 9u, 1.f - model.albedo, 1.f - turbidityRem,
										 solarElevation, rgbs[i] + (9 * 6 * (intTurbidity - 1)));
			bake_hosek_sky_configuration(model.configs[j][i], j, 9u, model.albedo, 1.f - turbidityRem,
										 solarElevation, rgbs[i] + (9 * 6 * 10 + 9 * 6 * (intTurbidity - 1)));
			if(intTurbidity < 10) {
				bake_hosek_sky_configuration(model.configs[j][i], j, 9u, 1.f - model.albedo, turbidityRem,
											 solarElevation, rgbs[i] + (9 * 6 * intTurbidity));
				bake_hosek_sky_configuration(model.configs[j][i], j, 9u, model.albedo, turbidityRem,
											 solarElevation, rgbs[i] + (9 * 6 * 10 + 9 * 6 * intTurbidity));
			}
		}
		// Radiance
		model.radiances[i] = 0.f;
		bake_hosek_sky_configuration(model.radiances[i], 0u, 1u, 1.f - model.albedo, 1.f - turbidityRem,
									 solarElevation, rads[i] + (6 * (intTurbidity - 1)));
		bake_hosek_sky_configuration(model.radiances[i], 0u, 1u, model.albedo, 1.f - turbidityRem,
									 solarElevation, rads[i] + (6 * 10 + 6 * (intTurbidity - 1)));
		if(intTurbidity < 10) {
			bake_hosek_sky_configuration(model.radiances[i], 0u, 1u, 1.f - model.albedo, turbidityRem,
										 solarElevation, rads[i] + (6 * intTurbidity));
			bake_hosek_sky_configuration(model.radiances[i], 0u, 1u, model.albedo, turbidityRem,
										 solarElevation, rads[i] + (6 * 10 + 6 * intTurbidity));
		}
	}
}

