void bake_hosek_sky_configuration(float& result, const unsigned i,
								  const unsigned count, const float albedo,
								  const float turbidityRem, const float solarElevation,
								  const float *data) {
		//(1-t).^3* A1 + 3*(1-t).^2.*t * A2 + 3*(1-t) .* t .^ 2 * A3 + t.^3 * A4;
	result +=
		albedo * turbidityRem
		* (std::pow(1.f - solarElevation, 5.f) * data[i] +
			5.f  * std::pow(1.f - solarElevation, 4.f) * solarElevation * data[i + count] +
			10.f * std::pow(1.f - solarElevation, 3.f) * std::pow(solarElevation, 2.f) * data[i + 2u * count] +
		   10.f * std::pow(1.f - solarElevation, 3.f) * std::pow(solarElevation, 2.f) * data[i + 2u * count] +
			5.f * (1.f - solarElevation) * std::pow(solarElevation, 4.f) * data[i + 4u * count] +
			std::pow(solarElevation, 5.f) * data[i + 5u * count]);
}

